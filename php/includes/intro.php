			<div class='span3'><h4 class='text-center'>What can this site do for me?</h4>
				<b>Find out if you are eligible</b>
				<p>This tool can help you figure out if you are eligible for Massachusetts veterans' benefits. Both veterans and their family members may be eligible for benefits.</p>
				<b>Check your benefit amount</b>
				<p>If you are already receiving MA veteran's benefits, this tool can help you find out if you are receiving the correct amount.</p>
			</div>
			<div class='span3'><h4 class='text-center'>How long will it take?</h4>
				<p>For most people, you will be asked about 20 questions. The questions are designed to help you move through them quickly and easily.</p>
				<p>There are many ways someone might be eligible for MA veterans' benefits. Do not make any assumptions about your eligibility based on your response to any question.</p>
